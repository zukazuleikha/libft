#include "libft.h"

size_t	ft_strlcpy(char *dst, char const *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size > 0)
	{
		while (src[i] && i < (size -1))
		{
			dst[i] = src [i];
			i++;
		}
		dst[i] = 0;
	}
	while (src[i])
	i++;
	return (i);
}
